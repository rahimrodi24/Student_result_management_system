

function toggleSecondLevelList() {
    var secondLevelList = document.querySelector(".second-level-list");
    if (secondLevelList.style.display === "none") {
        secondLevelList.style.display = "block";
    } else {
        secondLevelList.style.display = "none";
    }
} 
